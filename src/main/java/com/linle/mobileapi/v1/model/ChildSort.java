package com.linle.mobileapi.v1.model;

public class ChildSort {
	
	private Long childSortId;
	
	private String childSortName;
	
	private int childSortNums;

	public Long getChildSortId() {
		return childSortId;
	}

	public void setChildSortId(Long childSortId) {
		this.childSortId = childSortId;
	}

	public String getChildSortName() {
		return childSortName;
	}

	public void setChildSortName(String childSortName) {
		this.childSortName = childSortName;
	}

	public int getChildSortNums() {
		return childSortNums;
	}

	public void setChildSortNums(int childSortNums) {
		this.childSortNums = childSortNums;
	}
	

}
